package Model;

import java.util.Date;

public class HocPhan {
	private String MaHocPhan;
	private String TenHocPhan;
	private String MaBoMon;
	private int SoTinChi;
	private Date ThoiGian;
	private String GVQuanLyhocPhan;
	public String getMaHocPhan() {
		return MaHocPhan;
	}
	public void setMaHocPhan(String maHocPhan) {
		MaHocPhan = maHocPhan;
	}
	public String getTenHocPhan() {
		return TenHocPhan;
	}
	public void setTenHocPhan(String tenHocPhan) {
		TenHocPhan = tenHocPhan;
	}
	public String getMaBoMon() {
		return MaBoMon;
	}
	public void setMaBoMon(String maBoMon) {
		MaBoMon = maBoMon;
	}
	public int getSoTinChi() {
		return SoTinChi;
	}
	public void setSoTinChi(int soTinChi) {
		SoTinChi = soTinChi;
	}
	public Date getThoiGian() {
		return ThoiGian;
	}
	public void setThoiGian(Date thoiGian) {
		ThoiGian = thoiGian;
	}
	public String getGVQuanLyhocPhan() {
		return GVQuanLyhocPhan;
	}
	public void setGVQuanLyhocPhan(String gVQuanLyhocPhan) {
		GVQuanLyhocPhan = gVQuanLyhocPhan;
	}
	
	
	
}
